// This program will draw a face
// using the println method in the System.out object

public class U1A1 // begin the class
{
	public static void main(String[] args) // main method
	{
		System.out.print("  /////\n"); // hair
		System.out.println(" |     |"); // forehead
		System.out.println(" | \" \" |"); // eyebrows
		System.out.println(" | o o |"); // eyes
		System.out.println("(|  ^  |)"); // nose and ears
		System.out.println(" | \\_/ |"); // mouth
		System.out.println("  -----"); // chin
	} // end the method
} // end the class

